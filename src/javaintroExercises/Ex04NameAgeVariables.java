package javaintroExercises;

/**
 * A program to demonstrate the declaration and usage of variables to store a
 * name and an age. It prints out the name and age using a simple print statement-
 */
public class Ex04NameAgeVariables {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // VARIABLES FOR NAME AND AGE
        String myName = "Nora";
        int myAge = 25;

        // PRINTING
        System.out.println("My name is " + myName + " and I am " + myAge + " years old");
    }

}
